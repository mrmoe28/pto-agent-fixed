// Helper function to get counties by region
export function getCountiesByRegion(region: string) {
  return georgiaCounties.filter(county => county.region === region);
}

// Helper function to search counties by name or city
export function searchCounties(searchTerm: string) {
  const term = searchTerm.toLowerCase();
  return georgiaCounties.filter(county =>
    county.name.toLowerCase().includes(term) ||
    county.majorCities.some(city => city.toLowerCase().includes(term))
  );
}

// Helper function to get all unique regions
export function getRegions() {
  return [...new Set(georgiaCounties.map(county => county.region))];
}

// Helper function to get county by major city
export function getCountyByCity(cityName: string) {
  const city = cityName.toLowerCase();
  return georgiaCounties.find(county =>
    county.majorCities.some(majorCity => majorCity.toLowerCase() === city)
  );
}